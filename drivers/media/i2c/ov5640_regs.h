/*
 * This list of registers was painstakingly retrieved from the datasheet.
 *
 * BUT!
 *
 * The datasheet tries hard to obfuscate and not document a lot of registers,
 * either by marking them as DEBUG or by skipping addresses. These registers
 * are sometimes needed in the driver, and also sometimes provide a non-zero
 * value at power-on.
 *
 * As a result, not all _CRTLXX or DEBUGXX registers have the same XX number
 * as the datasheet, they instead match the final two digits of the address.
 *
 * Also, the datasheet only has register names counting decimally, which is
 * astounding in those cases where the digits in the register names started
 * out matching the last digits of the address.
 */
